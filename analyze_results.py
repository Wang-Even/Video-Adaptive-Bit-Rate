import argparse
import csv
import os

import numpy as np

import load_trace


LOG_COLUMNS = [
    'time', 'bitrate', 'buffer', 'rebuffer',
    'chunk_size', 'download_time', 'reward'
]


def read_log(path):
    rows = []
    with open(path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            values = [float(x) for x in line.split()]
            rows.append(dict(zip(LOG_COLUMNS, values)))
    return rows


def trace_stats(times, bandwidth):
    bw = np.array(bandwidth)
    diffs = np.diff(bw)
    relative_drop = 0.0
    if len(diffs) > 0:
        previous = np.maximum(bw[:-1], 1e-9)
        relative_drop = np.max(np.maximum(-diffs, 0) / previous)

    mean = float(np.mean(bw))
    std = float(np.std(bw))
    return {
        'avg_bw': mean,
        'max_bw': float(np.max(bw)),
        'min_bw': float(np.min(bw)),
        'std_bw': std,
        'range_bw': float(np.max(bw) - np.min(bw)),
        'abrupt_drop': relative_drop >= 0.30,
        'highly_volatile': std / mean >= 0.50 if mean > 0 else False,
    }


def summarize_log(rows):
    bitrate = np.array([row['bitrate'] for row in rows])
    rebuffer = np.array([row['rebuffer'] for row in rows])
    reward = np.array([row['reward'] for row in rows])
    smoothness = np.abs(np.diff(bitrate))
    return {
        'avg_bitrate': float(np.mean(bitrate)),
        'total_rebuffer': float(np.sum(rebuffer)),
        'avg_rebuffer': float(np.mean(rebuffer)),
        'avg_smoothness': float(np.mean(smoothness)) if len(smoothness) else 0.0,
        'total_reward': float(np.sum(reward)),
        'avg_reward': float(np.mean(reward)),
    }


def ensure_matplotlib():
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise SystemExit(
            'matplotlib is required for plotting. Run: pip install -r requirements.txt'
        ) from exc
    return plt


def plot_trace(plt, name, times, bandwidth, output_dir):
    plt.figure(figsize=(9, 4))
    plt.plot(times, bandwidth)
    plt.xlabel('time (s)')
    plt.ylabel('bandwidth (Mbps)')
    plt.title(name + ' bandwidth')
    plt.tight_layout()
    plt.savefig(os.path.join(output_dir, name + '_bandwidth.png'), dpi=150)
    plt.close()


def plot_log(plt, algorithm, trace_name, rows, output_dir):
    times = [row['time'] for row in rows]

    plt.figure(figsize=(9, 4))
    plt.plot(times, [row['buffer'] for row in rows])
    plt.xlabel('time (s)')
    plt.ylabel('buffer (s)')
    plt.title(algorithm + ' ' + trace_name + ' buffer')
    plt.tight_layout()
    plt.savefig(os.path.join(output_dir, algorithm + '_' + trace_name + '_buffer.png'), dpi=150)
    plt.close()

    plt.figure(figsize=(9, 4))
    plt.step(times, [row['bitrate'] for row in rows], where='post')
    plt.xlabel('time (s)')
    plt.ylabel('bitrate (Kbps)')
    plt.title(algorithm + ' ' + trace_name + ' bitrate')
    plt.tight_layout()
    plt.savefig(os.path.join(output_dir, algorithm + '_' + trace_name + '_bitrate.png'), dpi=150)
    plt.close()


def infer_algorithm(log_name):
    parts = log_name.split('_')
    if len(parts) >= 3:
        return parts[2]
    return 'unknown'


def build_arg_parser():
    parser = argparse.ArgumentParser(description='Analyze ABR traces and simulation logs.')
    parser.add_argument('--trace-folder', default=load_trace.COOKED_TRACE_FOLDER)
    parser.add_argument('--traces', nargs='*', default=None,
                        help='Trace names to analyze. Defaults to the first 3 sorted traces.')
    parser.add_argument('--result-dirs', nargs='*', default=[],
                        help='Result directories generated by bba.py or rb.py.')
    parser.add_argument('--output-dir', default='./analysis_output')
    return parser


def main():
    args = build_arg_parser().parse_args()
    os.makedirs(args.output_dir, exist_ok=True)

    trace_files = args.traces
    if trace_files is None:
        trace_files = sorted(os.listdir(args.trace_folder))[:3]

    all_time, all_bw, names = load_trace.load_trace(args.trace_folder, trace_files)
    plt = ensure_matplotlib()

    stats_path = os.path.join(args.output_dir, 'trace_stats.csv')
    with open(stats_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=[
            'trace', 'avg_bw', 'max_bw', 'min_bw', 'std_bw',
            'range_bw', 'abrupt_drop', 'highly_volatile'
        ])
        writer.writeheader()
        for times, bandwidth, name in zip(all_time, all_bw, names):
            row = {'trace': name}
            row.update(trace_stats(times, bandwidth))
            writer.writerow(row)
            plot_trace(plt, name, times, bandwidth, args.output_dir)

    metrics_path = os.path.join(args.output_dir, 'abr_metrics.csv')
    with open(metrics_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=[
            'algorithm', 'trace', 'avg_bitrate', 'total_rebuffer',
            'avg_rebuffer', 'avg_smoothness', 'total_reward', 'avg_reward'
        ])
        writer.writeheader()
        for result_dir in args.result_dirs:
            for filename in sorted(os.listdir(result_dir)):
                if not filename.startswith('log_sim_'):
                    continue
                path = os.path.join(result_dir, filename)
                rows = read_log(path)
                if not rows:
                    continue
                algorithm = infer_algorithm(filename)
                prefix = 'log_sim_' + algorithm + '_'
                trace_name = filename[len(prefix):]
                row = {'algorithm': algorithm, 'trace': trace_name}
                row.update(summarize_log(rows))
                writer.writerow(row)
                plot_log(plt, algorithm, trace_name, rows, args.output_dir)

    print('wrote', stats_path)
    print('wrote', metrics_path)
    print('plots in', args.output_dir)


if __name__ == '__main__':
    main()
